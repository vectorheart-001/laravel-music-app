    {
        $track_id = $request ->input('track_id');
        $comment_data = [
            'content' => $request->input('comment'),
            'track_id' => $request->input('track_id'),
            'user_id'=> Auth::id(),
        ];
        Comment::create($comment_data);


        return redirect()->route('tracks.show',$track_id)
            ->with('success','Comment posted successfully');



    }

